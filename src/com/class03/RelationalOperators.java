package com.class03;

public class RelationalOperators {

	public static void main(String[] args) {
		
		int a=10;
		int b=7;
		
		System.out.println(a>b);//true
		System.out.println(a<b);//false
		System.out.println(a==b);//false
		System.out.println(a!=b);//true; != means not equal
		
		boolean result=a>=b;
		boolean result1=a<=b;
		System.out.println(result);//true
		System.out.println(result1);//false
		
		int c=20;
		int d=29;
		
		System.out.println(c==d);//false; we are checking equality
		System.out.println(c=d);//29; assigning value d to c
		System.out.println(c);//29
		System.out.println(d);//29
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
