package com.class05;

public class IfNoBraces {

	public static void main(String[] args) {
	
		String day="friday";
		
		//if without braces
		
		if (day.equals("friday"))
        System.out.println("Today is a movie day");
		
		System.out.println("I have a class");
			
			
		//if with braces
		
		if (day.equals("friday"))
		System.out.println("Today is a movie day");
		
		System.out.println("I have a class");
		
		
			
	}

}
