package com.class07;

public class LoopIntro {

	public static void main(String[] args) {
		
	int time=7; //to do 5 Hello 12-7=5
	if(time<12) {
		System.out.println("Hello");//executes 1 time
	}
		
	while (time<12) {
		System.out.println("Hello");//executes forever
		time++;
	}
			
		
		
		
	
			
		
		
		
		
		
		
		
		
		

	}

}
